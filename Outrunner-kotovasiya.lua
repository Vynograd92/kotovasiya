-- Генерация юнитов для T0 (начальная зона, уникальные юниты 1 уровня без 2х клеток)
function unitsMercT0(race)
    local base_units = {
		-- империя
        'g003uu5001', 'g000uu0006', 'g000uu0011', 'g000uu0001', 'g000uu0008',
		--гномы
        'g000uu0036', 'g000uu0026', 'g004uu5039', 'g000uu0033',
		-- легионы
		'g000uu0052','g000uu0062',
		-- орды нежити
		'g000uu0080', 'g001uu7539', 'g000uu0086', 'g000uu0092','g000uu0078',
		--эльфы
		'g000uu8031', 'g000uu8014', 'g000uu8025', 'g000uu8018',
    }

    local race_excludes = {
        [Race.Human]   = { 'g003uu5001', 'g000uu0006', 'g000uu0011', 'g000uu0001', 'g000uu0008' },
        [Race.Dwarf]   = { 'g000uu0036', 'g000uu0026', 'g004uu5039', 'g000uu0033' },
        [Race.Undead]  = { 'g000uu0052','g000uu0062' },
        [Race.Heretic] = { 'g000uu0080', 'g001uu7539', 'g000uu0086', 'g000uu0092','g000uu0078' },
        [Race.Elf]     = { 'g000uu8031', 'g000uu8014', 'g000uu8025', 'g000uu8018' },
    }

    local unitsm = {}
    local i = 1
    local excludes = race_excludes[race] or {}
    local exclude_map = {}

    for _, id in ipairs(excludes) do
        exclude_map[id] = true
    end

    for _, id in ipairs(base_units) do
        if not exclude_map[id] then
            unitsm[i] = { id = id, level = 1, unique = true }
            i = i + 1
        end
    end

    return unitsm
end

