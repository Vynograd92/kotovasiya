	elseif radn == 3 then
	return what4
	elseif radn == 4 then
	return what5
	end
end

-- случайный
function rnd(...)
    return (select(math.random(select('#', ...)), ...))
end

-- перемешать таблцу
function mix(tblmg)
		for i = #tblmg, 2, -1 do
			local j = math.random(i)
			tblmg[i], tblmg[j] = tblmg[j], tblmg[i]
		end
end

-- перебор всей таблицы (когда записей хватает)
function rndy(tabl)
	local x = math.random(1, #tabl)
	local y = tabl[x]
	table.remove(tabl, x)
	return y
end


-- проверка на банку на жезлы для лавки т1
function rndybj(tabl)
	local x = math.random(1, #tabl)
	local y = tabl[x]
	local x2 = x + 1
	local y2 = tabl[x2]
	if y ~= 'g001ig0524' then
		table.remove(tabl, x)
		return y
	else
		table.remove(tabl, x2)
		return y2
	end
end

